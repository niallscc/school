package cs460.webdnd.client.mvc.controller;

/**
 * Commands will be the master of the project. They will do everything that
 * needs to be done. They will implement all the running code, while the Model
 * will do the 'record keeping'.
 * 
 * @author Theodore Schnepper
 */
public interface ICommand {
	/***
	 * All Commands must implements this so it can be invoked by the
	 * Controller/Model.
	 */
	void run();
}