package cs460.webdnd.client.mvc.view;

import cs460.webdnd.client.mvc.model.IState;

/**
 * A view to be registered with the Model. Views are meant only to update the
 * visual aspects of feedback.
 * 
 * Concern: With Google Web Toolkit, it may be rather difficult to separate the
 * Model and View.
 * 
 * @author Theodore Schnepper
 */
public interface IView {
	/**
	 * All Views must implement this so they have the proper information
	 * required to update their view.
	 */
	void Update(IState state);
}